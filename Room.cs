using UnityEngine;

public class Room : MonoBehaviour
{
    [Header("Basic Info")]
    public string roomName;

    //--- ??????????????? Temperature, EMF Level ??? ???? ?????????????? ---
    public float temperature;
    public int emfLevel;
    public bool canUseSpiritBox;

    //--- ????????????????? ? ---
    public bool canGhostWriting;
    public bool canFingerprint;  // ??????? UV Light

    // ???????? ResetEvidence() ??????????????????????? false (??????? default)
    public void ResetEvidence()
    {
        // ?????????????????????????????? 0 ???????????
        temperature = 20f;
        emfLevel = 0;
        canUseSpiritBox = false;
        canGhostWriting = false;
        canFingerprint = false;
    }
    private void OnTriggerEnter(Collider other)
    {
        Debug.Log("Something entered room " + roomName + ": " + other.name);

        // ????????????? SpiritBox ??????????????????????? SpiritBox ???
        SpiritBox spiritBox = other.GetComponent<SpiritBox>();
        if (spiritBox != null && canUseSpiritBox)
        {
            Debug.Log("Spirit Box is in this room: " + roomName);
        }
    }

    private void OnTriggerExit(Collider other)
    {
        Debug.Log("Something exited room " + roomName + ": " + other.name);
    }
}
